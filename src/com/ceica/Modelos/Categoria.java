package com.ceica.Modelos;

public class Categoria {
    private int id;
    private String nombre;

    public Categoria() {
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
